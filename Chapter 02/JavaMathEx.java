public class JavaMathEx{
    public static void main(String args[]){
        int x = 5;
        int y = 6;
        System.out.println("Max is = "+ Math.max(x, y));
        System.out.println("Max is = "+ Math.min(x, y));
        System.out.println("Max is = "+ Math.multiplyExact(x, y));
        System.out.println("Max is = "+ Math.divideExact(x, y));
        System.out.println("Max is = "+ Math.pow(x, y));
    }
}